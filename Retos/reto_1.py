#INGRESO DE DATOS 
altura = float(input("Ingrese la altura del rectangulo: "))
base = float(input("Ingrese el ancho del rectangulo: "))
#AREA DE UN RECTANGULO
area = base * altura;
print("area del rectangulo : ",area)
#PERIMETRO DE UN RECTANGULO
perimetro = 2*(base + altura);
print("perimetro del rectangulo : ",perimetro)
#SUPERFICIE DE UN RECTANGULO
superficie = base * altura;
print("perimetro del rectangulo : ",superficie)